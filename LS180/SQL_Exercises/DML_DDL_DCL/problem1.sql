DML - Data Manipulaiton Language
  - Used to manipulate data w/ CRUD principles; 
    - INSERT, SELECT, UPDATE, DELETE
  - Can add, change, remove data from a table

DDL - Data Definition Language
  - Used in the creation of tables and information relating
    to the schema of the table, such as table creation, column
    names, altering the table in some format, removing tables etc.
  - CREATE table_name, DROP table_name
  - ALTER TABLE table_name
  